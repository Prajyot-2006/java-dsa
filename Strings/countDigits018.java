import java.util.Scanner;

public class countDigits018 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        String s = n + "";
        System.out.println(s.length());

        double a = sc.nextDouble();
        String s1 = a + "";
        System.out.println(s1.length());
    }
}
