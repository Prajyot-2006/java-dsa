public class StringAndInt016 {
    public static void main(String[] args) {
        // String + int 
        String s = "eren";
        int a = 10;
        s = s + a;
        System.out.println(s);

        // String + char
        String s1 = "mikasa";
        char ch = 'p';
        s1 = s1 + ch;
        System.out.println(s1);

        // String + String 
        String s2 = "prajyot";
        String s3 = "jore";
        s2 = s2 + s3;
        System.out.println(s2);

        System.out.println("raghav" + 10 + 20);
        System.out.println(10+20+"raghav");
    }
}
